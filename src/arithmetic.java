public class arithmetic {
    public static void main(String[] args){
        int x = 10;
        int y = 12;
        int z;

        z = x+y;
        System.out.println(z);
        z = x-y;
        System.out.println(z);
        z = x*y;
        System.out.println(z);
        z = 10/12;
        System.out.println(z);
        z = 10%12;
        System.out.println(z);
    }
}
